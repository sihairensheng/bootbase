package com.bluefox.spring.bootbase.config;

//接口返回数据实体
public class TomcatConfig {

    private  String  port;

    private  String code;

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
